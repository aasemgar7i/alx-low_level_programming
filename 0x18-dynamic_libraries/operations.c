int add(int q, int e);
int sub(int q, int e);
int mul(int q, int e);
int div(int q, int e);
int mod(int q, int e);

int add(int q, int e)
{
	return (q + e);
}

int sub(int q, int e)
{
        return (q - e);
}

int mul(int q, int e)
{
        return (q * e);
}

int div(int q, int e)
{
        return (q / e);
}

int mod(int q, int e)
{
        return (q % e);
}
